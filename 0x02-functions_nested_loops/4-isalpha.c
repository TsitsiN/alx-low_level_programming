/**
 * _isalpha - print function that checks for alphabetic character - Entry point
 *
 * @c: c is an ASCII integer
 *Description: 'print parameters'
 *
 *
 * Return: 1 if it is a lowercase, uppercase and 0 otherwise (Success)
*/

int _isalpha(int c)
{
if ((c >= 97 && c <= 122) || (c >= 65 && c <= 90))
return (1);
else
return (0);
}
